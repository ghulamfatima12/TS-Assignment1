"use strict";
let favFruit = ["bananas", "apple", "mango"];
for (let k = 0; k < favFruit.length; k++) {
    if (favFruit[k] === "bananas") {
        console.log("I really like bananas");
    }
    else if (favFruit[k] === "apple") {
        console.log("I really like apple");
    }
    else if (favFruit[k] === "mango") {
        console.log("I really like mango");
    }
}
